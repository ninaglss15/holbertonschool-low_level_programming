#include "main.h"
#include <unistd.h>
#include <stdio.h>
/**
 * print_line - Prints the alphabet in lowercase and then in uppercase.
 *
 * Return: Always 0.
 *
 *@n: frjfnuhuyguigujkns
 */
void print_line(int n)
{
	int idx;

	if (n > 0)
	{
	for (idx = 0; idx < n; idx++)
	{
	_putchar('_');
	}
	}
	_putchar('\n');
}
